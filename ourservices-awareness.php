                                <p>
								Imagine monkeys coming to your society, but not at your window panes, rather at the nearby tree which has beared delicious fruits in spring! Imagine an Indian Rock Python, basking on a rock and people passing by without being afraid! Imagine your dustbins remaining undisturbed at night, because there are no surplus rats! Seems far-fetched in Mumbai, right? It is, to be honest. But we must move closer towards such dreams, that is Coexistence through Conservation.<br><br>
							From rats being pests, to overpopulation of pigeons, from the infamous Macaque(monkey) menace, to snakes in your car engines, from rampant cutting of trees, to destroying mangrove habitats, from illegal trade of star tortoises, to leopards entering peripheral human settlements for food, this city has seen it all. Innumerous human-wildlife conflicts and many daily rescues happen all around the city, helping us wade towards coexistence but we often keep making the water more murkish ourselves. A rescuer rescues an Indian rat snake (Ptyas mucosa) from a settlement and not ten metres away is a man throwing a bag full of waste on a pile of garbage. Don’t be surprised if you see the rescuer frequenting the place often. Garbage attracts rats, in turn, snakes. Feeding monkeys is an act of devotion to Lord Hanuman. But it becomes a conflict when the entire Vaanar sena is in your premises. Summer periods are brutal for birds because in urban cities, water sources are scarce. The Mithi river, which flows through Mumbai, and the Powai lake have been polluted by factories releasing toxic chemicals without any proper pre-disposal treatment. The mangroves have been cleared for building infrastructures and airports! And then we suffer from water lodging on the streets and roads and rails even.<br><br>
							There is no single group, community, organisation at question here. We are all in it together. The Sanjay Gandhi National Park, often termed as the lungs of Mumbai, is the only National Park in the world to be surrounded by a city on three sides and accounts for 20% of geographical area. Mumbai is the only city in the world where humans live close to leopards at around 100 metres. Mumbai is one of the few cities which has water on three sides and strong tectonic plates. Mumbai is much more than what we can possibly strive to make it! And yet, in this Urban Wildlife paradise, the lack of awareness has left us to succumb to our actions and their consequences.
		
