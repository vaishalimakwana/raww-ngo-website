                            <div class="title">Honeybees and Conflicts</div>
							
							
                            <div class="text">
                               We all have heard the term “busy as a bee”. This could not be farther from the truth! Does pollination come to mind? Yes! Bees play a vital role in pollination [1], which involves transferring pollen grain from the male part of a flower to the female part of another flower. The result of their valuable “buzzing activity” is very fruitful. The honey they make is vital for other animals [2]. <br><br>

Unfortunately, bees today are facing the brunt of urban human activities. A wonderful research paper by Gurudas Nulkar, et al, [3] covers conflicts in great detail. Typically conflicts in India remind us of tigers or leopards. However, these are more violent in nature and there is a need to address “silent ones”, which honeybees in India heavily face. <br><br>
As population in India increases and so does urbanisation, encroaching into wildlife territories in inevitable. Honeybees actually deal with two kinds of conflicts, silent and violent. Let us explore the silent category first. Silent conflicts are those which are not reported by media or which humans get involved in without their knowledge [3]. The following things affect honeybees. Firstly, phone tower radiation. With more citizens of India getting access to mobile phones, phone towers are on the rise. The radiation emitted however disturbs the bees. Next is pesticide spraying. Pesticides are sprayed on urban gardens to keep pests away. However, many of them are toxic and bees who travel from flower to flower, pick these up on their bodies. When they return to their hives, this directly affects their young ones. Another is the cutting down of old native trees and replacing them with new exotic trees. This disturbs the balance of flora which means bees cannot go back and have to resort to making beehives on buildings. <br><br>

Violent conflicts involve humans and wildlife in a line of war. Many houses in India and in Mumbai have beehives. Sadly, these are sprayed with toxins so they can be removed. Alsom humans get afraid of stinging by bees. However, bees do not actively seek to sting as doing so results in their death. Also there exists paranoia and is a result of poor information on part of people, false rumours, and unnecessary panic upon confrontation.<br><br>

Mr. Johnson Jacob shines a light in the middle of the dark situation with honeybees [4]. He set up an Apiary farm in Aarey, Goregaon and is the only beekeeper in Mumbai. His wonderful initiative has helped supply honey for communities. I am hopeful that more people will take such steps in the future to secure the future of honeybees.
<br><br>
Our response and approach differs in violent and silent situations. Silent conflicts can be resolved by NGOs running several awareness programs, where citizens are educated on the steps they need to take ahead and where they should be more careful. It is important that local and state governments support NGOs on these missions. We need to follow a holistic approach for this. Citizens, wildlife departments and NGOs all have to work hand in hand to help each other. This cross-functional teamwork will ensure that humans come to a strong understanding with bees and respect them.
<br><br>
In the end, wildlife was here many years before humans evolved on Earth. It is only right that we value honeybees. Connecting with nature also has a lot of health benefits in the long run. Finally, any animal that disappears from Earth affects the food chain as a whole, which can have drastic consequences. Let them buzz, no more fuss and allow them to be the hard workers that they are! <br><br>
								
