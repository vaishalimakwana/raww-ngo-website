                                        <div class="title">RAWW Rescue Process</div>
                                      
                                  
                                  
                                       <ul class="bullet_styl_nos">
										<li>The caller/informer contacts RAWW on helpline number (+91) 7666680202</li>
<li>RAWW Helpline first collects the available information, contact details,address/ location of distress, type of species, nature of distress etc</li>
<li>Immediately the helpline intimates and directs the rescue teams with these details. The rescuer who is available or the one who is close to the site affirms his position and reaches out to the distress call as follows</li>
<li>The rescuer calls the caller and reconfirms the situation and any further updates which was not reported earlier</li> 
<li>The rescue team leaves to respond to the distress </li>
<li>Based on the feed from the caller, the rescue team reaches the spot with appropriate equipment. On reaching the site, the situation is analyzed</li>
<li>The rescue team takes necessary action depending on various factors which keep changing with the situations on the ground</li> 
<li>Every rescued species which is taken into custody is examined by the vet,  the ones declared fit are released back to their suitable habitat, the ones which are abandoned or cannot be reunited are fostered, the injured ones are treated, all these activities initiated are in coordination with the forest department</li> 
 <li>The details of every distress call responded by our team is intimated to the RAWW Rescue Teams database. The successful ones are recorded in the rescue book maintained by the team which is periodically shared with the forest department on phone call every and email every 24 hours and hard copies in 48 hours</li>

										
										
