                            <div class="text">
                               A clean environment, climate change, sustainability. These terms have always played a vital role in our society, but in the past few years they have been getting much more attention. This article’s focus though will be on the interactions of disturbed environments and animals in India, with an emphasis on water and land pollution. Let us discuss environment issues on account of "World Clean-up Day" on September 19, 2020. And we begin!<br><br>
								<div class="font-bold">Water Pollution</div>
Water pollution is a major issue in India. In the Ulhasnagar region of Thane district [1], many industries dump their effluents without treating them. This has caused the river to be extremely polluted in some regions and a worrisome issue is that people depend on such water bodies for their daily activities. The story continues for the rivers in Mumbai as well, like the Mithi river that is 15 times more polluted than the safety limit. [2]<br><br>
Aquatic animals and their predators are badly affected by such contaminants . This even affects sea wildlife for that matter. In fact, mercury poisoning is a major issue in oceans of the world which hurts fish and people who consume them [3].<br><br>
The next type of water pollution is due to fishing nets. The "Resqink Association for Wildlife Welfare" [4] has covered this issue through a pressing description on Olive Ridley Turtles. Ghost nests or discarded fishing nets are not very visible to sea wildlife. Turtles entangled in them can injure themselves and even worse die due to lack of food or injuries. Apart from nets, beachside garbage is also hurting our ecosystem. Several animals have been found with various items in their bodies.<br><br>
<div class="font-bold">Land Pollution</div>
Land garbage presents multiple problems. First garbage attracts rodents, and this brings reptiles like snakes in conflict with humans [5]. With rapid urbanisation in India, garbage also leads to more stray dogs and with rapid urbanisation into forests, leopards come into the picture. The human-leopard conflict is very well known in India.
Burning garbage is also a major problem. Many plastic items release noxious fumes into the atmosphere, and it is known that low air quality drives animals (especially birds and bees) away from urban dwellings where they settled.<br><br>
<div class="font-bold">What can we do to address this and what has already been going on?</div>
							<ul class="bullet_styl">	
<li>For starters, the MIDC (Maharashtra Industrial Development Corporation) and the Pollution board are working together to address the issue of industrial effluents [1]. While they will control the industries, it our job to raise awareness amongst ourselves about the pollution we may cause. The Ganges river in North India has got very polluted due to human activities.</li>
<li>It must be ensured that fishermen keep control of their nets at all times. Conducting awareness sessions will help but there needs to be co-operation with the fishermen, NGOs, and the coastal management & protection teams.</li>
<li>On Earth day in particular millions of volunteers participate in cleaning initiatives every year. This also took place in India [6]. Organizing such drives where the citizens come together for a single day to clean a beach is a wonderful idea. Let us help out our friendly aquatic neighbours this way please!</li>
<li>Garbage must be disposed and treated properly as surroundings with less garbage will minimize human-wildlife conflicts and reduce pollution. Thankfully, with the concerns growing about climate change, poor quality plastic bags are slowly disappearing and being replaced by paper bags. Recycling is growing across the world and this will take care of some of the garbage.</li>
								</ul>								
